#2.Assignment Operator
#As => : =, +=, -=, *=, /=, %=
a=10
#แอสซายแวลู่
b = a
print("Assignment Value:",b)#=>:10
#แอด แอ่น แอดซายแวลู๋
b += a # b=b+a
print("Assignment Value:",b)#=>:20

b -= a # b=b-a
print("Assignment Value:",b)#=>:10

b *= a # b=b*a
print("Assignment Value:",b)#=>:100

b <<= a # b=b<<a
print("Assignment Value:",b)

b /= a # b=b*a
print("Assignment Value:",b)